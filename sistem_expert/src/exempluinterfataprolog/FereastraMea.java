     public void setOptiuni(String optiuni){
        intervalFactor.setEnabled(false);
        intervalFactor.setValue(100);
        this.variante.removeAll();
        this.variante.setLayout(new BoxLayout(this.variante, BoxLayout.PAGE_AXIS));
        optiuni=optiuni.trim();
        optiuni=optiuni.substring(1, optiuni.length()-1);
        optiuni=optiuni.trim();
        String[] vect_opt=optiuni.split(";");
        
        ButtonGroup grup = new ButtonGroup();
        for (final String item : vect_opt) {
            final String aux;
            if(item.charAt(0) == '\'' && item.charAt(item.length() - 1) == '\'')
                aux = item.substring(1, item.length() - 1);
            else
                aux = item;
           
            
            
            JRadioButton b = new JRadioButton("<html><body style='width:800; word-wrap:break-word'><p>" + aux + "</p></body></html>");
            b.setBackground(Color.white);
            b.setFont(new Font("Arial Rounded mt bold", 0, 28));
            b.setIcon(new ImageIcon("unchecked.png", ""));
            b.setSelectedIcon(new ImageIcon("checked.png", ""));
            
            b.addActionListener(new java.awt.event.ActionListener() {
                @Override
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                   
                    try {
                        System.out.println(item + "(fc=" + intervalFactor.getValue() + ")");
                        conexiune.expeditor.trimiteSirSicstus(item + "(fc=" + intervalFactor.getValue() + ")");
                    } catch (InterruptedException ex) {
                        Logger.getLogger(FereastraMea.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                            
            });
            grup.add(b);
            this.variante.add(b);
        }
        
        
        this.variante.repaint();
        this.variante.revalidate();
        this.revalidate();
        this.repaint();
        
        javax.swing.SwingUtilities.invokeLater(new Runnable() {
            public void run() { 
                jScrollPane1.getVerticalScrollBar().setValue(0);
            }
        });

    }  

     public void adaugaSolutie(String solutie){  
        if(!Fereastra.AFISAT_SOLUTII)
        {
            removeAllPanels();
            
            if(solutie.equals("nu"))
            {
                this.add(panou_fara_rezultate);
                this.repaint();
                this.revalidate();
                return;
            }
           
            this.add(panou_rezultat);
            this.repaint();
            this.revalidate();
            solutii = new ArrayList<>();
            Fereastra.AFISAT_SOLUTII = true;
        } 
        
        if(solutie.equals("gata"))
        {
            numar_rezultate.setText(String.valueOf(solutii.size()));
            System.out.println(solutii.size());
            this.repaint();
            this.revalidate();
            return;
        }
        
        String[] valori = solutie.split("\\$");
        for(String aux:valori)
        {
            System.out.println(aux);
        }
        String[] proprietati = valori[3].substring(1, valori[3].length() - 1).split("%");
        
        Map<String, String> colectie = new HashMap<>();
        for(String pereche : proprietati)
        {
            String [] elemente = pereche.split("#");
            colectie.put(elemente[0], elemente[1]);
        }
        
        Solutie sol = new Solutie(valori[0], valori[1], valori[2], colectie, valori[4]);
        solutii.add(sol);
    } 
     
     
