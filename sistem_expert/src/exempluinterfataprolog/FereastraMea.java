    private void incarcaToateFisiereleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_incarcaToateFisiereleActionPerformed
        FereastraMea.AFISAT_SOLUTII = false;
        String fr = fisierReguli.getText();
        String fc = fisierConstrangeri.getText();
        String fd = fisierDescrieri.getText();
        fisierReguli.setEnabled(false);
        fisierConstrangeri.setEnabled(false);
        fisierDescrieri.setEnabled(false);
        alegeFisierReguli.setEnabled(false);
        alegeFisierDescrieri.setEnabled(false);
        alegeFisierConstrangeri.setEnabled(false);
        String dir = System.getProperty("user.dir");
        dir = dir.replace("\\", "/");
        try {
            conexiune.expeditor.trimiteMesajSicstus("director('" + dir + "')");
            conexiune.expeditor.trimiteMesajSicstus("incarca(" + fr + ", " + fc + ", " + fd + ")");
            rezolvaTestul.setEnabled(true);
        } catch (InterruptedException ex) {
            Logger.getLogger(FereastraMea.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_incarcaToateFisiereleActionPerformed

